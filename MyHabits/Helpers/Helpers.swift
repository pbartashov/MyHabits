import UIKit

func creatTitleTextAttributes() -> [NSAttributedString.Key : Any] {
    let paragraphStyle = NSMutableParagraphStyle()
    paragraphStyle.lineHeightMultiple = 1.08

    let attributes: [NSAttributedString.Key : Any] = [.font: Fonts.SFProTextSemibold17,
                                                      .kern: -0.41,
                                                      .paragraphStyle: paragraphStyle
    ]
    
    return attributes

}
